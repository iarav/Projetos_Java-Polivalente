    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int usuario=0;
        if (jRadioButton1.isSelected()){
            usuario=0;
        }
        if (jRadioButton2.isSelected()){
            usuario=1;
        }
        if (jRadioButton3.isSelected()){
            usuario=2;
        }
        java.util.Random rand = new java.util.Random();
        int computador = rand.nextInt(3);
        
       if (usuario==computador){
            jTextField1.setText("Empatou!");
       }else{
          if(usuario==0){
            if (computador==2){
                jTextField1.setText("Você venceu!");
            }else{
                jTextField1.setText("Você perdeu!");
            }
        }
          if(usuario==1){
            if (computador==0){
                jTextField1.setText("Você venceu!");
            }else{
                jTextField1.setText("Você perdeu!");
            }
        }
          if(usuario==2){
            if (computador==1){
                jTextField1.setText("Você venceu!");
            }else{
                jTextField1.setText("Você perdeu!");
            }
        }
       } 
       if(computador==0){
        jTextField2.setText("O computador escolheu Pedra");   
       }else{
           if(computador==1){
            jTextField2.setText("O computador escolheu Papel");   
           }else{
            jTextField2.setText("O computador escolheu Tesoura");   
           }
       }
    }//GEN-LAST:event_jButton1ActionPerformed

