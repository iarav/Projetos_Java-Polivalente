    private void Login(){
        Usuario u = new Usuario();
        rs = u.consultar();
        String nomeDigitado = jTextField1.getText();
        String senhaDigitado = new String (jPasswordField1.getPassword());
        String nome,senha;
        int i = 0;
        
        try {
            rs.beforeFirst();

            while (rs.next()) {
                String[] registro = new String[2];
                registro[0] = rs.getString("Nome");
                registro[1] = rs.getString("Senha");
                
                nome = registro[0];
                senha = registro[1];
                
                if(nome.equals(nomeDigitado)){
                    if(senha.equals(senhaDigitado)){
                        new TelaPrincipal().setVisible(true);
                        dispose();
                        i = 1;
                    }
                }             
            }
            
            if(i==0){
                JOptionPane.showMessageDialog(this, "Usuário ou senha inválido!!!");
                jTextField1.requestFocus();
                jTextField1.selectAll();
                jPasswordField1.setText("");
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }    
    }

