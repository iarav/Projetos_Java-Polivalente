    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        ResultSet rs;
        Produto P = new Produto();

        if(jTextField1.getText().equals("")||jTextField2.getText().equals("")||jTextField3.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Algum campo está vazio!! Preencha-o");
        }else{
            rs = P.pesquisar(jTextField1.getText());

            try {
                if (rs.next()) {
                    String operacao = String.valueOf(jComboBox1.getSelectedItem());
                    String CodPro = jTextField1.getText();
                    int Qtde = Integer.valueOf(jTextField2.getText());
                    int SaldoAtual = rs.getInt("Saldo");
                    int SaldoForncido = Integer.valueOf(jTextField2.getText());
                    int SaldoFinal = SaldoAtual;
                    if(operacao.equals("+")){
                        SaldoFinal = SaldoAtual + SaldoForncido;
                    }else{
                        if(operacao.equals("-")){
                            SaldoFinal = SaldoAtual - SaldoForncido;
                        }
                    }
                    
                    String dataEmUmFormato = jTextField3.getText(); 
                    String[] s = dataEmUmFormato.split("/"); 
                    String novaData = s[2]+"/"+s[1]+"/"+s[0];

                    Movimento M = new Movimento();
                    M.setCodProduto(CodPro);
                    M.setOperacao(operacao);
                    M.setQtde(Qtde);
                    M.setData(novaData);

                    M.incluir();

                    JOptionPane.showMessageDialog(this, "Essa movimentação foi cadastrada com sucesso!","Inserido com sucesso!", WIDTH);

                    Produto p = new Produto(rs.getString("CodProduto"), rs.getString("Descricao"), rs.getString("Unidade"),SaldoFinal,rs.getDouble("VrCompra"),rs.getDouble("VrVenda"));
                    p.alterar();

                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    
                    Listar();
                } else {
                    JOptionPane.showMessageDialog(null, "Produto não encontrado! TENTE NOVAMENTE!!");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextField1.selectAll();
                }
            } catch (SQLException ex) {
                Logger.getLogger(CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

