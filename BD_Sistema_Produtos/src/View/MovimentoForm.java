    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ResultSet rs;
        int bt;
        int codMovi = Integer.valueOf(jTextField4.getText());
        String codPro = jTextField1.getText();
        String qtde = jTextField2.getText();
        String data = jTextField3.getText();
        String operacao = String.valueOf(jComboBox1.getSelectedItem());
        
         bt = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir isso? A movimentação de código "
                + codMovi + "?", "Confirmação", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (bt == JOptionPane.YES_OPTION) {
            Movimento M = new Movimento();
            M.setCodMovi(codMovi);
            try {
                M.excluir();
            } catch (SQLException ex) {
                Logger.getLogger(MovimentoForm.class.getName()).log(Level.SEVERE, null, ex);
            }

            Produto P = new Produto();

            if(jTextField1.getText().equals("")||jTextField2.getText().equals("")||jTextField3.getText().equals("")||jTextField4.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Algum campo está vazio!! Preencha-o");
            }else{
                rs = P.pesquisar(jTextField1.getText());

                try {
                    if (rs.next()) {
                        int SaldoAtual = rs.getInt("Saldo");
                        int SaldoForncido = Integer.valueOf(qtde);
                        int SaldoFinal = SaldoAtual;
                        if(operacao.equals("+")){
                            SaldoFinal = SaldoAtual - SaldoForncido;
                        }else{
                            if(operacao.equals("-")){
                                SaldoFinal = SaldoAtual + SaldoForncido;
                            }
                        }

                        Produto p = new Produto(rs.getString("CodProduto"), rs.getString("Descricao"), rs.getString("Unidade"),SaldoFinal,rs.getDouble("VrCompra"),rs.getDouble("VrVenda"));
                        p.alterar();

                        jTextField1.setText("");
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jTextField4.setText("");
                        JOptionPane.showMessageDialog(null, "Excluido com sucesso");

                    } else {
                        JOptionPane.showMessageDialog(null, "Produto não encontrado! TENTE NOVAMENTE!!");
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jTextField1.selectAll();
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(MovimentoForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Não foi excluido");
        }
        Listar();
    }//GEN-LAST:event_jButton2ActionPerformed

