    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Botão Novo
        if(jButton2.getText().equals("Novo")){
            jTextField1.setText("");
            jButton2.setText("Gravar");
        }else{

        if(jTextField1.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Você não adicionou nenhum texto!");
            }else{
            Estado est = new Estado(0, jTextField1.getText());
            try {
                est.novo();
            } catch (SQLException ex) {
                Logger.getLogger(EstadojFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
                JOptionPane.showMessageDialog(null, "Incluido com sucesso");
                jButton2.setText("Novo");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            //Botão >
            if(rs.isBeforeFirst())
                rs.first();
        } catch (SQLException ex) {
            Logger.getLogger(EstadojFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            if(rs.next()){
                jTextField1.setText(rs.getString("Nome"));
                jTable1.setRowSelectionInterval(rs.getRow()-1,rs.getRow()-1);
            }} catch (SQLException ex) {
            Logger.getLogger(EstadojFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        //Botão <
        try {
            if(rs.isAfterLast())
                rs.last();
        } catch (SQLException ex) {
            Logger.getLogger(EstadojFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            if(rs.previous()){
                jTextField1.setText(rs.getString("Nome"));
                jTable1.setRowSelectionInterval(rs.getRow()-1,rs.getRow()-1);
            } } catch (SQLException ex) {
            Logger.getLogger(EstadojFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

