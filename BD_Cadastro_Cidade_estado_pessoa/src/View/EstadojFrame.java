    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        //Botão Atualizar
        Estado st = new Estado();
        rs = st.Listar();

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        try {
            rs.beforeFirst();
        } catch (SQLException ex) {
            Logger.getLogger(EstadojFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            while (rs.next()) {
                String[] registro = new String[2];
                registro[0] = rs.getString("CodEstado");
                registro[1] = rs.getString("Nome");
                dtm.addRow(registro);
            }
        } catch (SQLException ex) {
            Logger.getLogger(EstadojFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTable1.setModel(dtm);
        jTable1.setRowSelectionInterval(0, 0);
        try {
            rs.first();
        } catch (SQLException ex) {
            Logger.getLogger(EstadojFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //Botão Alterar
        int id;
        int ls;
        if(jTextField1.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Você não adicionou nenhum texto!");
        }else{
            ls = jTable1.getSelectedRow();
            DefaultTableModel grid = (DefaultTableModel) jTable1.getModel();
            id = Integer.valueOf(grid.getValueAt(ls,0).toString());
            Estado st = new Estado(id,jTextField1.getText());
            try {
                st.alterar();
            } catch (SQLException ex) {
                Logger.getLogger(EstadojFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Alterado com sucesso");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Botão Excluir
        int id;
        int ls;
        int bt;
        String est;
        ls=jTable1.getSelectedRow();
        DefaultTableModel grid = (DefaultTableModel) jTable1.getModel();
        est = grid.getValueAt(ls,1).toString();
        bt=JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir isso? O estado "+
        est + "?","Confirmação",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);
        if(bt==JOptionPane.YES_OPTION){
            id = Integer.valueOf(grid.getValueAt(ls,0).toString());
            Estado st = new Estado(id,jTextField1.getText());
            try {
                st.excluir();
            } catch (SQLException ex) {
                Logger.getLogger(EstadojFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Excluido com sucesso");
        }else{
        JOptionPane.showMessageDialog(null, "Não foi excluido");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

