    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //Botão Novo
       if(jButton3.getText().equals("Novo")){
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jButton3.setText("Gravar");
        }else{
            if(jTextField2.getText().equals("")||jTextField3.getText().equals("")||jTextField4.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Você não adicionou nenhum texto!");
            }else{
                Cidade c = (Cidade) cbCidade.getSelectedItem(); 
                Pessoa ps = new Pessoa(0, jTextField2.getText(),jTextField3.getText(),
                c.getCodCidade(),c.getCodEstado());
                try {
                    ps.novo();
                } catch (SQLException ex) {
                    Logger.getLogger(PessoajFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(null, "Incluido com sucesso");
                jButton3.setText("Novo");
            }
       }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Botão Atualizar
         Pessoa ps = new Pessoa();
         rs = ps.Listar();

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
         try {
             rs.beforeFirst();
         } catch (SQLException ex) {
             Logger.getLogger(PessoajFrame.class.getName()).log(Level.SEVERE, null, ex);
         }
         try {
             while (rs.next()) {
                 String[] registro = new String[5];
                 registro[0] = rs.getString("CodPessoa");
                 registro[1] = rs.getString("Nome");
                 registro[2] = rs.getString("Fone");
                 registro[3] = rs.getString("CodCidade");
                 registro[4] = rs.getString("CodEstado");
                 dtm.addRow(registro);
             }
         } catch (SQLException ex) {
             Logger.getLogger(PessoajFrame.class.getName()).log(Level.SEVERE, null, ex);
         }
        jTable1.setModel(dtm);
        jTable1.setRowSelectionInterval(0, 0);
         try {
             rs.first();
         } catch (SQLException ex) {
             Logger.getLogger(PessoajFrame.class.getName()).log(Level.SEVERE, null, ex);
         }
    }//GEN-LAST:event_jButton4ActionPerformed

