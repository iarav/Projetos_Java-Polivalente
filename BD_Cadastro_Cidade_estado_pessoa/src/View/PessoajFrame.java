        private void setCbCidade(int CodCidade){
        for(int i=0; i<cbCidade.getItemCount();i++){
            Cidade c = (Cidade) cbCidade.getItemAt (i);
            if(c.getCodCidade()== CodCidade){
                cbCidade.setSelectedIndex(i);
                break;
            }
        }
    }
    
    
    
